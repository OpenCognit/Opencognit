#!/usr/bin/env node
/**
 * OpenCognit CLI — Zero-Config Onboarding Wizard
 *
 * npx @opencognit/cli   →  Interactive setup in <30 seconds
 *
 * What it does:
 *   1. Environment check (Node >= 20, port 3201 free)
 *   2. Creates ~/.opencognit/opencognit.db + applies all migrations
 *   3. Seeds: company "Meine Firma", CEO agent "Aria", 1 demo task
 *   4. Generates .env with secure random secrets
 *   5. Starts the OpenCognit server (from current project root)
 *   6. Opens http://localhost:3201 in the default browser
 */

import fs from 'fs';
import path from 'path';
import os from 'os';
import { execSync, spawn } from 'child_process';
import net from 'net';
import readline from 'readline';
import crypto from 'crypto';

// ── ANSI color helpers (zero-dep) ────────────────────────────────────────────
const c = {
  cyan:   (s: string) => `\x1b[36m${s}\x1b[0m`,
  green:  (s: string) => `\x1b[32m${s}\x1b[0m`,
  yellow: (s: string) => `\x1b[33m${s}\x1b[0m`,
  red:    (s: string) => `\x1b[31m${s}\x1b[0m`,
  bold:   (s: string) => `\x1b[1m${s}\x1b[0m`,
  dim:    (s: string) => `\x1b[2m${s}\x1b[0m`,
  reset:  '\x1b[0m',
};

// ── Banner ────────────────────────────────────────────────────────────────────
function printBanner() {
  console.clear();
  console.log(c.cyan(c.bold(`
  ╔═══════════════════════════════════════╗
  ║                                       ║
  ║   ◈  O P E N C O G N I T  ◈          ║
  ║   Autonomous Agent Platform           ║
  ║   v0.9  —  Zero-Config Setup          ║
  ║                                       ║
  ╚═══════════════════════════════════════╝
`)));
}

// ── Readline helpers (zero-dep interactive prompts) ──────────────────────────
const rl = readline.createInterface({ input: process.stdin, output: process.stdout });

function ask(question: string): Promise<string> {
  return new Promise(resolve => {
    rl.question(c.cyan('? ') + c.bold(question) + ' ', answer => resolve(answer.trim()));
  });
}

function askYN(question: string, defaultYes = true): Promise<boolean> {
  const hint = defaultYes ? '[Y/n]' : '[y/N]';
  return ask(`${question} ${c.dim(hint)}`).then(a => {
    if (a === '') return defaultYes;
    return a.toLowerCase().startsWith('y');
  });
}

// ── Step utilities ────────────────────────────────────────────────────────────
let stepIndex = 0;
function step(name: string) {
  stepIndex++;
  process.stdout.write(`\n  ${c.cyan(`[${stepIndex}]`)} ${name}... `);
}
function ok(detail = '') {
  console.log(c.green('✓') + (detail ? c.dim(` ${detail}`) : ''));
}
function warn(msg: string) {
  console.log(c.yellow('⚠') + c.dim(` ${msg}`));
}
function fail(msg: string): never {
  console.log(c.red('✗') + ` ${msg}`);
  rl.close();
  process.exit(1);
}

// ── Environment checks ────────────────────────────────────────────────────────
function checkNodeVersion() {
  step('Node.js version');
  const [major] = process.versions.node.split('.').map(Number);
  if (major < 20) {
    fail(`Node.js 20+ required. Current: ${process.versions.node}. Install from https://nodejs.org`);
  }
  ok(`v${process.versions.node}`);
}

function checkPort(port: number): Promise<boolean> {
  return new Promise(resolve => {
    const srv = net.createServer();
    srv.once('error', () => resolve(false));
    srv.once('listening', () => { srv.close(); resolve(true); });
    srv.listen(port, '127.0.0.1');
  });
}

async function checkPortAvailability() {
  step('Port 3201 availability');
  const free = await checkPort(3201);
  if (!free) {
    warn('Port 3201 is occupied. OpenCognit may already be running.');
    const proceed = await askYN('Continue anyway?', false);
    if (!proceed) fail('Aborted. Free port 3201 and try again.');
  } else {
    ok('free');
  }
}

// ── Path setup ────────────────────────────────────────────────────────────────
const HOME_DIR    = path.join(os.homedir(), '.opencognit');
const DB_PATH     = path.join(HOME_DIR, 'opencognit.db');
const ENV_PATH    = path.join(process.cwd(), '.env');

function ensureDataDir() {
  step('Data directory');
  if (!fs.existsSync(HOME_DIR)) fs.mkdirSync(HOME_DIR, { recursive: true });
  ok(HOME_DIR);
}

// ── .env generation ───────────────────────────────────────────────────────────
function generateEnv(apiKey: string): string {
  const jwtSecret   = crypto.randomBytes(32).toString('hex');
  const encKey      = crypto.randomBytes(32).toString('hex');
  const sessionSeed = crypto.randomBytes(16).toString('hex');

  return [
    `# OpenCognit — generated by opencognit CLI`,
    `# ${new Date().toISOString()}`,
    '',
    `PORT=3201`,
    `NODE_ENV=development`,
    '',
    `# Database (override for PostgreSQL: postgresql://user:pass@host/db)`,
    `# DATABASE_URL=`,
    `OPENCOGNIT_DB_PATH=${DB_PATH}`,
    '',
    `# Security`,
    `JWT_SECRET=${jwtSecret}`,
    `ENCRYPTION_KEY=${encKey}`,
    `SESSION_SECRET=${sessionSeed}`,
    '',
    `# LLM Provider (add your key here or configure in Settings)`,
    apiKey ? `ANTHROPIC_API_KEY=${apiKey}` : `# ANTHROPIC_API_KEY=`,
    `# OPENROUTER_API_KEY=`,
    `# OPENAI_API_KEY=`,
    '',
    `# Optional: Telegram Bot Integration`,
    `# TELEGRAM_BOT_TOKEN=`,
  ].join('\n');
}

// ── Database setup ────────────────────────────────────────────────────────────
async function setupDatabase() {
  step('SQLite database');

  // Point the server to our home-dir DB
  process.env['OPENCOGNIT_DB_PATH'] = DB_PATH;

  ok(DB_PATH);
}

// ── Seed: company + CEO + demo task ──────────────────────────────────────────
function generateId(): string {
  return crypto.randomUUID();
}

function seedStarterData(companyName: string, adminEmail: string, adminName: string, adminPasswordHash: string) {
  // Import better-sqlite3 and seed directly (avoids full server startup for seeding)
  // This runs before the server starts so the first login works immediately.

  try {
    // Dynamic import — better-sqlite3 must be available in the project's node_modules
    const projectRoot = findProjectRoot();
    const DatabaseModule = require(path.join(projectRoot, 'node_modules', 'better-sqlite3'));
    const Database = DatabaseModule.default || DatabaseModule;

    const db = new Database(DB_PATH);
    db.pragma('journal_mode = WAL');
    db.pragma('foreign_keys = ON');

    // Run all migrations first
    const migrationsDir = path.join(projectRoot, 'server', 'db', 'migrations', 'sqlite');
    if (!fs.existsSync(migrationsDir)) {
      console.log(c.yellow('\n  ⚠ Migrations directory not found. Skipping auto-migration.'));
      console.log(c.dim(`  Expected: ${migrationsDir}`));
      console.log(c.dim('  Run: npm run db:migrate'));
      db.close();
      return null;
    }

    db.exec(`
      CREATE TABLE IF NOT EXISTS _migrations (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT NOT NULL UNIQUE,
        applied_at TEXT NOT NULL
      );
    `);

    const applied = new Set<string>(
      db.prepare('SELECT name FROM _migrations').all().map((r: any) => r.name)
    );

    const migFiles = fs.readdirSync(migrationsDir)
      .filter((f: string) => f.endsWith('.sql'))
      .sort();

    let migrationsApplied = 0;
    for (const file of migFiles) {
      if (applied.has(file)) continue;
      const sql = fs.readFileSync(path.join(migrationsDir, file), 'utf-8');
      try {
        db.transaction(() => {
          db.exec(sql);
          db.prepare('INSERT INTO _migrations (name, applied_at) VALUES (?, ?)').run(file, new Date().toISOString());
        })();
        migrationsApplied++;
      } catch (e: any) {
        // Some migrations may fail if already applied via different path — log but continue
        console.log(c.dim(`\n  skip migration ${file}: ${e.message.slice(0, 80)}`));
      }
    }

    const now = new Date().toISOString();

    // Check if already seeded
    const existingCompany = db.prepare('SELECT id FROM unternehmen LIMIT 1').get();
    if (existingCompany) {
      db.close();
      return null; // Already has data
    }

    // Create admin user
    const userId = generateId();
    db.prepare(`
      INSERT INTO benutzer (id, name, email, passwort_hash, rolle, erstellt_am, aktualisiert_am)
      VALUES (?, ?, ?, ?, 'admin', ?, ?)
    `).run(userId, adminName, adminEmail.toLowerCase(), adminPasswordHash, now, now);

    // Create company
    const companyId = generateId();
    db.prepare(`
      INSERT INTO unternehmen (id, name, beschreibung, ziel, status, erstellt_am, aktualisiert_am)
      VALUES (?, ?, ?, ?, 'active', ?, ?)
    `).run(
      companyId,
      companyName,
      'Von OpenCognit CLI erstellt',
      'Demonstriere die Fähigkeiten autonomer Agenten',
      now, now
    );

    // Create CEO agent "Aria"
    const agentId = generateId();
    db.prepare(`
      INSERT INTO experten (
        id, unternehmen_id, name, rolle, titel, status,
        verbindungs_typ, is_orchestrator, zyklus_aktiv,
        system_prompt, budget_monat_cent, verbraucht_monat_cent,
        nachrichten_count, erstellt_am, aktualisiert_am,
        advisor_strategy, avatar_farbe
      ) VALUES (?, ?, 'Aria', 'CEO', 'Chief Executive Officer', 'active',
        'anthropic', 1, 1,
        'Du bist Aria, CEO und Orchestratorin. Du weist Aufgaben zu, verfolgst Ziele und koordinierst das Team. Antworte präzise und strategisch.',
        500, 0, 0, ?, ?, 'none', '#23CDCB')
    `).run(agentId, companyId, now, now);

    // Create agent permissions for CEO
    db.prepare(`
      INSERT INTO agent_permissions (
        id, expert_id, darf_aufgaben_erstellen, darf_aufgaben_zuweisen,
        darf_genehmigungen_anfordern, darf_genehmigungen_entscheiden,
        darf_experten_anwerben, erstellt_am, aktualisiert_am
      ) VALUES (?, ?, 1, 1, 1, 1, 1, ?, ?)
    `).run(generateId(), agentId, now, now);

    // Create demo task
    const taskId = generateId();
    db.prepare(`
      INSERT INTO aufgaben (
        id, unternehmen_id, titel, beschreibung, status, prioritaet,
        zugewiesen_an, erstellt_von, erstellt_am, aktualisiert_am
      ) VALUES (?, ?, ?, ?, 'todo', 'high', ?, 'board', ?, ?)
    `).run(
      taskId, companyId,
      'Analysiere dieses Projekt',
      `Willkommen bei OpenCognit! Dies ist deine erste Aufgabe.\n\n` +
      `Analysiere den aktuellen Projektstatus und erstelle einen strukturierten Bericht mit:\n` +
      `1. Überblick über vorhandene Ressourcen\n` +
      `2. Identifizierte nächste Schritte\n` +
      `3. Empfohlene Priorisierung\n\n` +
      `Erstelle danach 2-3 konkrete Folge-Tasks für das Team.`,
      agentId, now, now
    );

    // Create a company goal
    db.prepare(`
      INSERT INTO ziele (id, unternehmen_id, titel, beschreibung, ebene, status, fortschritt, erstellt_am, aktualisiert_am)
      VALUES (?, ?, ?, ?, 'company', 'active', 0, ?, ?)
    `).run(
      generateId(), companyId,
      'OpenCognit erfolgreich einrichten und ersten Workflow automatisieren',
      'Lerne das System kennen und starte deinen ersten autonomen Agenten-Workflow.',
      now, now
    );

    db.close();
    return { userId, companyId, agentId, taskId, migrationsApplied };
  } catch (e: any) {
    console.log(c.yellow(`\n  ⚠ Auto-seed fehlgeschlagen: ${e.message}`));
    console.log(c.dim('  Führe nach dem Start manuell npm run db:seed aus.'));
    return null;
  }
}

// ── Find project root (where package.json + server/ live) ────────────────────
function findProjectRoot(): string {
  let dir = process.cwd();
  for (let i = 0; i < 6; i++) {
    if (fs.existsSync(path.join(dir, 'package.json')) && fs.existsSync(path.join(dir, 'server'))) {
      return dir;
    }
    const parent = path.dirname(dir);
    if (parent === dir) break;
    dir = parent;
  }
  return process.cwd();
}

// ── Open browser ──────────────────────────────────────────────────────────────
function openBrowser(url: string) {
  try {
    const platform = process.platform;
    if (platform === 'darwin') execSync(`open "${url}"`, { stdio: 'ignore' });
    else if (platform === 'win32') execSync(`start "" "${url}"`, { stdio: 'ignore', shell: '/bin/sh' });
    else execSync(`xdg-open "${url}"`, { stdio: 'ignore' });
  } catch {
    // Browser open failed — URL shown in console anyway
  }
}

// ── Start server ──────────────────────────────────────────────────────────────
async function startServer(projectRoot: string): Promise<void> {
  const tsxBin = path.join(projectRoot, 'node_modules', '.bin', 'tsx');
  const serverEntry = path.join(projectRoot, 'server', 'index.ts');

  if (!fs.existsSync(serverEntry)) {
    fail(`Server not found at ${serverEntry}. Run this from the OpenCognit project root.`);
  }
  if (!fs.existsSync(tsxBin)) {
    fail(`tsx not found. Run npm install in the project root first.`);
  }

  const serverProcess = spawn(tsxBin, [serverEntry], {
    cwd: projectRoot,
    stdio: 'pipe',
    env: { ...process.env, NODE_ENV: 'development', OPENCOGNIT_DB_PATH: DB_PATH },
    detached: false,
  });

  return new Promise((resolve, reject) => {
    let ready = false;
    let output = '';

    serverProcess.stdout?.on('data', (data: Buffer) => {
      const text = data.toString();
      output += text;
      if (!ready && (text.includes('läuft auf') || text.includes('listening') || text.includes(':3201'))) {
        ready = true;
        resolve();
      }
    });

    serverProcess.stderr?.on('data', (data: Buffer) => {
      const text = data.toString();
      output += text;
      if (!ready && text.includes(':3201')) {
        ready = true;
        resolve();
      }
    });

    serverProcess.on('error', (e: Error) => {
      if (!ready) reject(e);
    });

    serverProcess.on('exit', (code: number) => {
      if (!ready) reject(new Error(`Server exited with code ${code}.\n${output.slice(-500)}`));
    });

    // Timeout: 20 seconds
    setTimeout(() => {
      if (!ready) {
        ready = true;
        resolve(); // Assume running even if we missed the startup log
      }
    }, 20000);
  });
}

// ── Wait for HTTP readiness ───────────────────────────────────────────────────
async function waitForHttp(url: string, maxAttempts = 15): Promise<boolean> {
  for (let i = 0; i < maxAttempts; i++) {
    try {
      await new Promise(r => setTimeout(r, 1000));
      const res = await fetch(`${url}/api/health`);
      if (res.ok) return true;
    } catch {}
  }
  return false;
}

// Generate a random one-time password at setup — never ship hardcoded credentials
import crypto from 'crypto';
const DEFAULT_PASSWORD = crypto.randomBytes(10).toString('base64url').slice(0, 14);

// ═══════════════════════════════════════════════════════════════════════════════
// MAIN
// ═══════════════════════════════════════════════════════════════════════════════
async function main() {
  printBanner();

  console.log(c.dim('  This wizard sets up OpenCognit in under 30 seconds.\n'));

  // ── Phase 1: Environment ──────────────────────────────────────────────────
  console.log(c.bold('\n  ━━ Phase 1: Environment Check ━━'));
  checkNodeVersion();
  await checkPortAvailability();
  ensureDataDir();

  const projectRoot = findProjectRoot();

  // ── Phase 2: Configuration ────────────────────────────────────────────────
  console.log(c.bold('\n  ━━ Phase 2: Configuration ━━'));

  let companyName = await ask('Company name (press Enter for "Meine Firma"):');
  if (!companyName) companyName = 'Meine Firma';

  let adminName = await ask('Your name (admin account):');
  if (!adminName) adminName = 'Admin';

  let adminEmail = await ask('Your email:');
  if (!adminEmail) adminEmail = 'admin@opencognit.local';

  console.log(c.dim(`  ℹ Generated password: ${c.bold(DEFAULT_PASSWORD)} — save this, change it in Settings after first login`));

  let apiKey = '';
  const hasApiKey = await askYN('Do you have an Anthropic API key?', false);
  if (hasApiKey) {
    apiKey = await ask('Paste your API key:');
  } else {
    console.log(c.dim('  → No key entered. You can add it later in Settings → API Keys.'));
    console.log(c.dim('  → Agents will use Ollama (if installed) or wait for a key.'));
  }

  // ── Phase 3: Write .env ───────────────────────────────────────────────────
  console.log(c.bold('\n  ━━ Phase 3: Setup ━━'));

  step('.env file');
  const envContent = generateEnv(apiKey);
  if (!fs.existsSync(ENV_PATH)) {
    fs.writeFileSync(ENV_PATH, envContent, 'utf-8');
    ok('created');
  } else {
    ok('already exists — not overwritten');
  }

  // Load .env into process.env for the server
  for (const line of envContent.split('\n')) {
    const match = line.match(/^([A-Z_][A-Z0-9_]*)=(.+)$/);
    if (match) process.env[match[1]] = match[2];
  }

  // ── Phase 4: Database + Seed ──────────────────────────────────────────────
  step('Database migrations');
  await setupDatabase();
  ok('SQLite + WAL mode');

  step('Seeding starter data');
  // Hash the generated password at runtime using bcryptjs from the project
  let DEFAULT_PASSWORD_HASH: string;
  try {
    const projectRoot = findProjectRoot();
    const bcrypt = require(path.join(projectRoot, 'node_modules', 'bcryptjs'));
    DEFAULT_PASSWORD_HASH = bcrypt.hashSync(DEFAULT_PASSWORD, 10);
  } catch {
    // Fallback: use SHA-256 if bcryptjs unavailable (server will re-hash on first login)
    DEFAULT_PASSWORD_HASH = crypto.createHash('sha256').update(DEFAULT_PASSWORD).digest('hex');
  }
  const seedResult = seedStarterData(companyName, adminEmail, adminName, DEFAULT_PASSWORD_HASH);
  if (seedResult) {
    ok(`Company "${companyName}" + CEO Aria + 1 demo task`);
    if (seedResult.migrationsApplied > 0) {
      console.log(c.dim(`    Applied ${seedResult.migrationsApplied} new migration(s)`));
    }
  } else {
    ok('existing data detected — skipped');
  }

  // ── Phase 5: Start server ─────────────────────────────────────────────────
  console.log(c.bold('\n  ━━ Phase 4: Starting Server ━━'));
  step('OpenCognit server');

  try {
    await startServer(projectRoot);
    ok('process started');
  } catch (e: any) {
    warn(`Server startup warning: ${e.message}`);
  }

  step('Waiting for HTTP readiness');
  const ready = await waitForHttp('http://localhost:3201');
  if (ready) ok('API responding');
  else warn('Server may still be starting — opening browser anyway');

  // ── Phase 6: Open browser ─────────────────────────────────────────────────
  step('Opening browser');
  openBrowser('http://localhost:3201');
  ok('http://localhost:3201');

  rl.close();

  // ── Done ──────────────────────────────────────────────────────────────────
  console.log(`
${c.green(c.bold('  ✓ OpenCognit is running!'))}

  ${c.bold('URL')}      http://localhost:3201
  ${c.bold('Email')}    ${adminEmail}
  ${c.bold('Password')} ${DEFAULT_PASSWORD}

  ${c.dim('Aria (CEO) will start working on your demo task automatically.')}
  ${c.dim('Change your password in Settings after first login.')}
  ${c.dim('Add API keys in Settings → API Keys to enable LLM agents.')}

  ${c.cyan('Press Ctrl+C to stop the server.')}
`);

  // Keep process alive while server runs
  process.on('SIGINT', () => {
    console.log(c.dim('\n  Shutting down...'));
    process.exit(0);
  });

  // Prevent immediate exit
  setInterval(() => {}, 1000);
}

main().catch(e => {
  console.error(c.red('\n  Fatal error:'), e.message);
  process.exit(1);
});
